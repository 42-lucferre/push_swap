#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	nbr;
	char	r;

	nbr = n;
	if (nbr < 0)
	{
		nbr *= -1;
		write(fd, "-", 1);
	}
	if (nbr > 9)
		ft_putnbr_fd((int)(nbr / 10), fd);
	r = nbr % 10 + '0';
	write(fd, &r, 1);
}
